/*
    View Engine Helps Us to Render Dynamic Content to the User. Where Content is User 
    Specific and Data Comes From Model.
*/

/*
    let's Take an Example of View Engine :-

    // Static Content WithOut View Engine
    <p>This is a Message<p>

    // Dynamic Content With the Help of View Engine
    let message = 'This is a Message'
    <p><%= message %><p>
    Here message is Variable of JavaScript Which Can Be change According to User
*/

/*
    Syntax Can be Differ According to the View Engine. Some Template View Engines :-
    1. PUG
    2. HandleBars
    3. EJS( Embedded JavaScript ) : EJS Very Similar TO HTML and Easy to Learn and Work With JS File. EJS is Combination of HTML and JavaScript.
*/